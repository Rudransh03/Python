# Stack implementation using Lists
stack = []

# append() function to push
stack.append('a')
stack.append('b')
stack.append('c')

print('initial Stack')
print(stack)

# pop() function to pop elements in LIFO order

print("\nElements popped from the stack are: ")
print(stack.pop())
print(stack.pop())


print("\nStack after elements popped are: ")
print(stack)
